var month= "January";

switch (month) {
    case "December":
    case "January":
    case "February":
        console.log("winter");
        break;
    case "March":
    case "April":
    case "May":
        console.log("spring");
        break;
    case "June":
    case "July":
    case "August":
        console.log("summer");
        break;
    case "September":
    case "October":
    case "November":
        console.log("fall");
        break;
    default:
        console.log("That's not a number");
}